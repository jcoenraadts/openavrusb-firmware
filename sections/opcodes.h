#ifndef OPCODES_H_
#define OPCODES_H_

//Section Definitions
#define S_UART          0x10            //      RS232 / UART
#define S_SPI           0x12            //      External SPI
#define S_I2C           0x14            //      I2C
#define S_GPIO          0x15            //      GPIO
#define S_PSU           0x18            //      Prog PSU
#define S_TIMER         0x1B            //      Timers
#define S_LCD			0x1C			//		HD4470 LCD
#define S_LED           0x30            //      LEDs
#define S_EEPROM        0x31            //      EEPROM
#define S_INTERNAL      0x3A            //      Internal calls

#endif /* OPCODES_H_ */
